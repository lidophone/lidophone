class User extends Authenticatable
{
    use HasApiTokens, HasFactory, Notifiable;
    use SoftDeletes;

    public const ADMIN_EMAILS = [
        'admin@lidofon.ru', // To local development & dev server
        'demkashadow@gmail.com',
        'dmitry@stepanov.biz',
        'emilentayler@gmail.com',
        'mrsatntplay@gmail.com',
        'nemizidko@gmail.com',
        'w.karankevich@gmail.com',
        'w3lifer@gmail.com',
    ];

    private static ?bool $isAdmin = null;

    /**
     * The attributes that are mass assignable.
     *
     * @var array<int, string>
     */
    protected $fillable = [
        'name',
        'email',
        'password',
    ];

    /**
     * The attributes that should be hidden for serialization.
     *
     * @var array<int, string>
     */
    protected $hidden = [
        'password',
        'remember_token',
    ];

    /**
     * The attributes that should be cast.
     *
     * @var array<string, string>
     */
    protected $casts = [
        'email_verified_at' => 'datetime',
        'password' => 'hashed',
    ];

    public static function isAdmin(): bool
    {
        if (!is_null(self::$isAdmin)) {
            return self::$isAdmin;
        }
        return in_array(Auth::user()?->email, self::ADMIN_EMAILS);
    }

    public static function isExpertMode(): bool
    {
        return Auth::user()->expert_mode;
    }
}
